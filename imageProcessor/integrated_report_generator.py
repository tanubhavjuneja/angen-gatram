#!/usr/bin/env python3
"""
Integrated Forensic Report Generator
Combines detailed SIEM-Tools style reporting with comprehensive analysis results.
Generates TXT, JSON, CSV, and HTML reports.
"""

import os
import json
import csv
from pathlib import Path
from datetime import datetime
from typing import Dict, List, Any, Optional
from io import StringIO


class IntegratedReportGenerator:
    def __init__(self, output_dir: str):
        self.output_dir = Path(output_dir)
        self.results: Dict[str, Any] = {}
        self.timestamp = datetime.now().isoformat()

    def load_results(self, results: Dict[str, Any]):
        self.results = results

    def generate_all_reports(self) -> Dict[str, str]:
        """Generate all report formats"""
        reports = {
            "txt": self.generate_text_report(),
            "json": self.generate_json_report(),
            "csv": self.generate_csv_report(),
            "html": self.generate_html_report(),
        }
        return reports

    def generate_text_report(self, output_file: Optional[str] = None) -> str:
        """Generate comprehensive text report"""
        metadata = self.results.get("analysis_metadata", {})
        summary = self.results.get("summary", {})
        
        integrated = self.results.get("integrated_analysis", {})
        integrated_summary = integrated.get("summary", {})
        
        data_wipe = self.results.get("data_wipe_analysis", {})
        hidden_volume = self.results.get("hidden_volume_analysis", {})
        
        partitions = self.results.get("partitions", [])

        report = []
        report.append("=" * 100)
        report.append("                    COMPREHENSIVE FORENSIC ANALYSIS REPORT")
        report.append("                    Generated by Angen Gatram Forensic Analyzer")
        report.append("=" * 100)
        report.append("")
        report.append(f"Analysis Date:     {metadata.get('analyzed_at', self.timestamp)}")
        report.append(f"Output Directory:  {metadata.get('output_directory', str(self.output_dir))}")
        report.append(f"Image Path:       {metadata.get('image_path', 'N/A')}")
        report.append("")

        # Partition Information
        report.append("-" * 100)
        report.append("                              PARTITION INFORMATION")
        report.append("-" * 100)
        for p in partitions[:10]:
            report.append(f"  {p.get('slot', 'N/A'):10} | Size: {p.get('size_sectors', 0) * 512 / (1024**3):.2f} GB | FS: {p.get('fs', 'unknown')}")
        report.append("")

        # Integrated Analysis Summary
        report.append("-" * 100)
        report.append("                    TIMESTAMP INTEGRITY ANALYSIS (SI/FN Comparison)")
        report.append("-" * 100)
        report.append(f"  Total Files Analyzed:    {integrated_summary.get('total_files_analyzed', 0)}")
        report.append(f"  Suspicious Files:        {integrated_summary.get('suspicious_files', 0)}")
        report.append(f"  [CRITICAL] Findings:    {integrated_summary.get('critical_count', 0)}")
        report.append(f"  [HIGH] Findings:        {integrated_summary.get('high_count', 0)}")
        report.append(f"  [MEDIUM] Findings:      {integrated_summary.get('medium_count', 0)}")
        report.append(f"  [LOW] Findings:        {integrated_summary.get('low_count', 0)}")
        report.append("")

        # Show critical findings
        critical_findings = integrated.get("critical_findings", [])
        if critical_findings:
            report.append("=" * 100)
            report.append("                         CRITICAL TIMESTAMP ANOMALIES")
            report.append("=" * 100)
            for i, finding in enumerate(critical_findings[:10], 1):
                report.append(f"\n#{i} FILENAME: {finding.get('filename', 'Unknown')}")
                report.append(f"    Record Number:    {finding.get('file_reference', 0)}")
                report.append(f"    Severity:        {finding.get('overall_severity', 'N/A')}")
                report.append(f"    Score:           {finding.get('overall_score', 0)}")
                
                si = finding.get("si_timestamps", {})
                fn = finding.get("fn_timestamps", {})
                if si.get("created") != "None":
                    report.append(f"    SI Created:      {si.get('created')}")
                if fn.get("created") != "None":
                    report.append(f"    FN Created:      {fn.get('created')}")
                
                delta = finding.get("si_fn_created_delta", 0)
                if delta > 0:
                    report.append(f"    SI/FN Delta:     {delta:.1f} seconds ({delta/86400:.2f} days)")
                
                for indicator in finding.get("indicators", [])[:3]:
                    report.append(f"    - {indicator.get('description', '')}")
            report.append("")

        # High findings
        high_findings = integrated.get("high_findings", [])
        if high_findings:
            report.append("=" * 100)
            report.append("                         HIGH SEVERITY ANOMALIES")
            report.append("=" * 100)
            for i, finding in enumerate(high_findings[:10], 1):
                report.append(f"\n#{i} {finding.get('filename', 'Unknown')} | Score: {finding.get('overall_score', 0)}")
                delta = finding.get("si_fn_created_delta", 0)
                if delta > 0:
                    report.append(f"    SI/FN Delta: {delta:.1f}s ({delta/86400:.2f} days)")
            report.append("")

        # Data Wipe Detection
        report.append("-" * 100)
        report.append("                          DATA WIPE DETECTION")
        report.append("-" * 100)
        if data_wipe.get("wipe_detected"):
            report.append("  [!] DATA WIPING DETECTED")
            report.append(f"  Confidence: {data_wipe.get('confidence', 0) * 100:.1f}%")
            report.append(f"  Summary: {data_wipe.get('summary', '')}")
            
            details = data_wipe.get("details", {})
            if details.get("partition_analysis"):
                for part in details["partition_analysis"]:
                    scan = part.get("partition_scan", {})
                    report.append(f"\n  Partition Analysis:")
                    report.append(f"    - High Entropy: {scan.get('high_entropy_percentage', 0):.1f}%")
                    report.append(f"    - Zero-Filled: {scan.get('zero_percentage', 0):.1f}%")
                    report.append(f"    - Method: {scan.get('method', 'unknown')}")
        else:
            report.append("  [+] No data wiping detected")
        report.append("")

        # Hidden Volume Detection
        report.append("-" * 100)
        report.append("                     HIDDEN VOLUME / ENCRYPTED CONTAINER")
        report.append("-" * 100)
        if hidden_volume.get("wipe_detected"):
            report.append("  [!] HIDDEN VOLUME OR ENCRYPTED CONTAINER DETECTED")
            report.append(f"  Summary: {hidden_volume.get('summary', '')}")
            
            for part in hidden_volume.get("partitions", [])[:5]:
                if part.get("hidden_volume_detected"):
                    report.append(f"\n  Partition: {part.get('partition_slot', 'N/A')}")
                    report.append(f"    Confidence: {part.get('confidence', 0) * 100:.1f}%")
                    report.append(f"    Method: {part.get('detection_method', 'unknown')}")
        else:
            report.append("  [+] No hidden volumes or encrypted containers detected")
        report.append("")

        # Recommendations
        report.append("=" * 100)
        report.append("                              RECOMMENDATIONS")
        report.append("=" * 100)
        report.append("")
        
        if integrated_summary.get("critical_count", 0) > 0:
            report.append("[!] IMMEDIATE ACTION REQUIRED")
            report.append("    - Critical timestamp manipulation detected")
            report.append("    - Isolate system for further investigation")
            report.append("    - Check for known timestomping tools")
            report.append("")
        
        if data_wipe.get("wipe_detected"):
            report.append("[!] DATA DESTRUCTION EVIDENCE")
            report.append("    - Evidence of secure deletion or file shredding")
            report.append("    - Investigate data loss prevention tools")
            report.append("")
        
        if hidden_volume.get("wipe_detected"):
            report.append("[!] ENCRYPTED CONTAINER DETECTED")
            report.append("    - Hidden volume or VeraCrypt container found")
            report.append("    - May contain hidden OS or sensitive data")
            report.append("")

        report.append("[+] Review all findings above for complete analysis.")
        report.append("")
        report.append("=" * 100)
        report.append(f"Report generated: {datetime.now().isoformat()}")
        report.append("=" * 100)

        report_text = "\n".join(report)
        
        if output_file:
            with open(output_file, "w") as f:
                f.write(report_text)
        
        return report_text

    def generate_json_report(self, output_file: Optional[str] = None) -> str:
        """Generate JSON report"""
        json_data = json.dumps(self.results, indent=2, default=str)
        
        if output_file:
            with open(output_file, "w") as f:
                f.write(json_data)
        
        return json_data

    def generate_csv_report(self, output_file: Optional[str] = None) -> str:
        """Generate CSV report of all findings"""
        output = StringIO()
        writer = csv.writer(output)
        
        writer.writerow(["Type", "Filename", "Severity", "Score", "Description", "Details", "Delta Seconds"])
        
        # Integrated findings
        integrated = self.results.get("integrated_analysis", {})
        for part_result in integrated.get("partition_results", []):
            for finding in part_result.get("findings", []):
                if finding.get("is_timestomped"):
                    writer.writerow([
                        "Timestomp",
                        finding.get("filename", ""),
                        finding.get("overall_severity", ""),
                        finding.get("overall_score", 0),
                        finding.get("indicators", [{}])[0].get("description", ""),
                        json.dumps(finding.get("evidence", {})),
                        finding.get("si_fn_created_delta", 0),
                    ])
        
        # Data wipe
        data_wipe = self.results.get("data_wipe_analysis", {})
        for indicator in data_wipe.get("indicators", []):
            writer.writerow([
                "Data Wipe",
                indicator.get("file_path", "N/A"),
                indicator.get("severity", ""),
                indicator.get("confidence", 0) * 100,
                indicator.get("description", ""),
                indicator.get("evidence", ""),
                0,
            ])
        
        csv_content = output.getvalue()
        
        if output_file:
            with open(output_file, "w", newline="") as f:
                f.write(csv_content)
        
        return csv_content

