def generate_integrated_report(output_dir: str, results: Dict[str, Any]) -> Dict[str, str]:
    """Generate all report formats"""
    generator = IntegratedReportGenerator(output_dir)
    generator.load_results(results)
    
    reports = generator.generate_all_reports()
    
    # Save reports to files
    txt_file = Path(output_dir) / "forensic_report.txt"
    json_file = Path(output_dir) / "forensic_report.json"
    csv_file = Path(output_dir) / "forensic_report.csv"
    html_file = Path(output_dir) / "forensic_report.html"
    
    with open(txt_file, "w") as f:
        f.write(reports["txt"])
    
    with open(json_file, "w") as f:
        f.write(reports["json"])
    
    with open(csv_file, "w", newline="") as f:
        f.write(reports["csv"])
    
    with open(html_file, "w") as f:
        f.write(reports["html"])
    
    return {
        "txt": str(txt_file),
        "json": str(json_file),
        "csv": str(csv_file),
        "html": str(html_file),
    }


if __name__ == "__main__":
    import sys
    
    if len(sys.argv) < 2:
        print("Usage: python integrated_report_generator.py <output_dir>")
        sys.exit(1)
    
    output_dir = sys.argv[1]
    
    results_file = Path(output_dir) / "integrated_forensic_analysis.json"
    if results_file.exists():
        import json
        with open(results_file) as f:
            results = json.load(f)
    else:
        results = {}
    
    reports = generate_integrated_report(output_dir, results)
    
    print("Reports generated:")
    for fmt, path in reports.items():
        print(f"  {fmt}: {path}")
